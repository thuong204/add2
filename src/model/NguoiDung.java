package model;

public class NguoiDung {
	private String tenDN;
	private String pass;
	
	public NguoiDung() {
	}
	public NguoiDung(String tenDN, String pass) {
		this.tenDN = tenDN;
		this.pass = pass;
	}
	public String getTenDN() {
		return tenDN;
	}
	public void setTenDN(String tenDN) {
		this.tenDN = tenDN;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	
	
	
	
	

}
